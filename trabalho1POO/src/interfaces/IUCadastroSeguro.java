    private void btnCadastroSeguroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroSeguroActionPerformed
        
        
        if( (txtCodigo.getText().length() == 0) || (txtNomeSeguradoura1.getText().length() == 0) || (txtAreaDescricao.getText().length() == 0) || (txtTipoSeguro.getText().length() == 0) || (txtValor.getText().length() == 0)){
            JOptionPane.showMessageDialog(null,"Preencha todos os campos!" , "Erro no Cadastro!", NORMAL);
            
        }else{
            String nomeSeguradoura = txtNomeSeguradoura1.getText();
            int codigoSeguro = Integer.parseInt(txtCodigo.getText());
            String descricao = txtAreaDescricao.getText();
            String tipo = txtTipoSeguro.getText();
            float valor = Float.parseFloat(txtValor.getText());
            
            Controlador control = new Controlador();
            Seguro novoSeguro = new Seguro(codigoSeguro, nomeSeguradoura, tipo, descricao, valor);
            control.addSeguro(novoSeguro);
        }
        

    }//GEN-LAST:event_btnCadastroSeguroActionPerformed

